// Найти максимальный элемент, а остальные обнулить с помощью цикла.

// Результат должен быть такой [0, 0, 0, 0, 0, 0, 178, 0, 0]

let arr = [15, 68, 123, 9, 0, 64, 178, 3, 26];
for (let i = 0; i < arr.length; i++){
    for (let j = 0; j < i; j++){
        if (arr[i] > arr[j]) {
            arr[j] = 0;
        }
        else {
            arr[i] = 0;
        }
    }
}

console.log(arr);